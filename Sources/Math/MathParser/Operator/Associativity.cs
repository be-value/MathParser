{
    /// <summary>
    /// Defines the associativity of arithmetic operators
    /// </summary>
    internal enum Associativity
    {
        /// <summary>
        /// left to right operator associativity
        /// </summary>
        Left2Right,

        /// <summary>
        /// right to left operator associativity
        /// </summary>
        Right2Left
    }
}
